
public class Calculator {

	public Calculator() {
		
	}
	
	public Integer sum(int a, int b) {
		return a + b;
		
	} 
	public Integer subtraction(int a, int b) {
		return a - b;
		
	} 
	public Integer division(int a, int b) {
		return a / b;
		
	} 
	public Integer multiplication(int a, int b) {
		return a * b;
		
	} 
	
	
}